package com.dhwooden.ep.yzj;


public class YzjException extends RuntimeException {


	public YzjException() {
		super();
	}

	public YzjException(String message) {
		super(message);
	}

	public YzjException(Throwable cause) {
		super(cause);
	}

	public YzjException(String message, Throwable cause) {
		super(message, cause);
	}
}
